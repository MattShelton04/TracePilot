//! Phase 1B.1 pilot: thin shim over [`tracepilot_tauri_bindings::specta_exports::export`].
//!
//! Run via `pnpm gen:bindings` (or
//! `cargo run -p tracepilot-tauri-bindings --bin gen-bindings` from the
//! repo root). Emits:
//!
//!   - `packages/client/src/generated/bindings.ts` — the specta-typed
//!     TypeScript client for the pilot allow-list (types + invoke wrappers).
//!   - `packages/client/src/generated/ipc-commands.json` — the full IPC
//!     command manifest (sorted), used by
//!     `packages/client/src/__tests__/commandContract.test.ts` to assert the
//!     TS-side `IPC_COMMANDS` registry stays in lock-step with the Rust
//!     source of truth at `crates/tracepilot-tauri-bindings/src/ipc_command_names.rs`.
//!
//! The heavy lifting for the `bindings.ts` side lives inside the lib crate so
//! that the hidden per-command macros generated by `#[specta::specta]` (which
//! are only reachable from within the defining crate) can be resolved by
//! `collect_commands!`.

use std::path::PathBuf;

use tracepilot_tauri_bindings::IPC_COMMAND_NAMES;

fn main() -> Result<(), Box<dyn std::error::Error>> {
    // Resolve output paths relative to the crate manifest so `cargo run` from
    // the repo root, a crate dir, or an IDE all work identically.
    let manifest_dir = PathBuf::from(env!("CARGO_MANIFEST_DIR"));
    let generated_dir = manifest_dir
        .join("..")
        .join("..")
        .join("packages")
        .join("client")
        .join("src")
        .join("generated");

    std::fs::create_dir_all(&generated_dir)?;

    let bindings_path = generated_dir.join("bindings.ts");
    tracepilot_tauri_bindings::specta_exports::export(&bindings_path)?;
    println!("wrote {}", bindings_path.display());

    let manifest_path = generated_dir.join("ipc-commands.json");
    write_ipc_command_manifest(&manifest_path)?;
    println!("wrote {}", manifest_path.display());

    Ok(())
}

/// Emit the full IPC command allowlist as a deterministic JSON array.
///
/// Serialised by hand (no `serde_json` dependency needed at bin scope) so
/// the output is stable, trailing-newline terminated, and trivially diffable.
/// The list is sorted to make equality assertions order-independent on the
/// TS side and to keep churn out of reviews when entries are added
/// mid-section in the Rust source.
fn write_ipc_command_manifest(out_path: &PathBuf) -> std::io::Result<()> {
    let mut names: Vec<&str> = IPC_COMMAND_NAMES.to_vec();
    names.sort_unstable();
    names.dedup();
    assert_eq!(
        names.len(),
        IPC_COMMAND_NAMES.len(),
        "IPC_COMMAND_NAMES contains duplicate entries"
    );

    let mut body = String::with_capacity(names.len() * 32);
    body.push_str("[\n");
    for (idx, name) in names.iter().enumerate() {
        body.push_str("  \"");
        body.push_str(name);
        body.push('"');
        if idx + 1 < names.len() {
            body.push(',');
        }
        body.push('\n');
    }
    body.push_str("]\n");

    std::fs::write(out_path, body)
}
